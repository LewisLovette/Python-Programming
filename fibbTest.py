x = 1
y = 1
count = 0

while True:
	print("\n\n-------------------------------------------------------------------------\nCount: "+str(count))
	z = x + y
	count += 1
	print("\n\n-------------------------------------------------------------------------\nCount: "+str(count))
	print("\n"+str(z))
	y = x + z
	count += 1
	print("\n\n-------------------------------------------------------------------------\nCount: "+str(count))
	print("\n"+str(y))
	x = y + z
	count += 1
	print("\n\n-------------------------------------------------------------------------\nCount: "+str(count))
	print("\n"+str(x))
